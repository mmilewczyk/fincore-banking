package com.matcodem.fincore.notification.adapter.out.sms;

import org.springframework.stereotype.Component;

import com.matcodem.fincore.notification.domain.model.Notification;
import com.matcodem.fincore.notification.domain.model.NotificationChannel;
import com.matcodem.fincore.notification.domain.port.out.ChannelSendException;
import com.matcodem.fincore.notification.domain.port.out.ChannelSender;
import com.twilio.exception.ApiException;
import com.twilio.rest.api.v2010.account.Message;
import com.twilio.type.PhoneNumber;

import lombok.RequiredArgsConstructor;
import lombok.extern.slf4j.Slf4j;

/**
 * SMS channel sender via Twilio.
 * <p>
 * SMS content rules:
 * - Max 160 chars for single segment (GSM-7 encoding)
 * - Long messages are split into multi-part SMS (charged per segment)
 * - We limit body to 155 chars to stay within single segment safely
 * <p>
 * Body = plain text (no HTML) - notification.getPayload().body() is already
 * plain text, generated by NotificationPayloadFactory.
 * <p>
 * Twilio error handling:
 * - 21211: invalid phone number -> DEAD_LETTER immediately (no retry)
 * - 21610: unsubscribed recipient -> DEAD_LETTER immediately
 * - Other errors -> transient, retry via outbox poller
 */
@Slf4j
@Component
@RequiredArgsConstructor
public class SmsChannelSender implements ChannelSender {

	private final SmsProperties smsProperties;

	private static final int SMS_BODY_MAX_LENGTH = 155;

	@Override
	public NotificationChannel channel() {
		return NotificationChannel.SMS;
	}

	@Override
	public void send(Notification notification) throws ChannelSendException {
		String toPhone = notification.getContact().phoneNumber();
		if (toPhone == null || toPhone.isBlank()) {
			throw new ChannelSendException("No phone number for userId=" + notification.getRecipientUserId());
		}

		String body = truncateForSms(notification.getPayload().body());

		try {
			Message message = Message.creator(
					new PhoneNumber(toPhone),
					new PhoneNumber(smsProperties.getFromNumber()),
					body
			).create();

			log.debug("SMS sent: sid={}, to={}, notificationId={}", message.getSid(), toPhone, notification.getId());

		} catch (ApiException ex) {
			int errorCode = ex.getCode();
			// 21211 = invalid phone, 21610 = unsubscribed - permanent failures
			if (errorCode == 21211 || errorCode == 21610) {
				throw new ChannelSendException(
						"SMS permanently undeliverable to %s (Twilio code %d): %s"
								.formatted(toPhone, errorCode, ex.getMessage()), ex);
			}
			throw new ChannelSendException(
					"Twilio API error (code=%d): %s".formatted(errorCode, ex.getMessage()), ex);
		}
	}

	private String truncateForSms(String body) {
		if (body == null) return "";
		return body.length() <= SMS_BODY_MAX_LENGTH
				? body
				: body.substring(0, SMS_BODY_MAX_LENGTH - 3) + "...";
	}
}